/**
 * Allowlist поддерживаемых почтовых доменов.
 *
 * Требование законодательства РФ: регистрация возможна только через
 * российские (поддерживаемые) почтовые сервисы. Email проходит проверку,
 * только если его доменная часть присутствует в этом множестве.
 *
 * Список намеренно вынесен в отдельный файл, чтобы новые домены можно было
 * добавлять сюда БЕЗ изменения логики валидатора (emailValidator.ts).
 *
 * ВНИМАНИЕ: pochta.ru (Почта России) оставлен как в исходном списке — это
 * скорее сайт почтовых/логистических услуг, чем провайдер личной email-почты.
 * Уточните у юриста, прежде чем полагаться на него как на «безопасный» домен.
 */
export const ALLOWED_EMAIL_DOMAINS: ReadonlySet<string> = new Set([
	// Яндекс
	"yandex.ru",
	"ya.ru",
	// VK / Mail.ru Group
	"mail.ru",
	"bk.ru",
	"list.ru",
	"inbox.ru",
	"internet.ru",
	"vk.com",
	"xmail.ru",
	// Rambler
	"rambler.ru",
	"ro.ru",
	"myrambler.ru",
	// Требует отдельной проверки (см. комментарий выше)
	"pochta.ru",
	// Добавляйте новые разрешённые домены сюда — валидатор менять не нужно.
]);

export const isAllowedEmailDomain = (domain: string): boolean =>
	ALLOWED_EMAIL_DOMAINS.has(domain.toLowerCase().trim());
