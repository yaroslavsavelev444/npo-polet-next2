/**
 * Строгая валидация ФИО получателя.
 *
 * Цель — не пропустить логин/никнейм/имя аккаунта вместо настоящего ФИО.
 * Поэтому требуем именно фамилию + имя (+ отчество опционально): 2–3 слова
 * кириллицей, каждое с заглавной буквы. Двойные фамилии через дефис разрешены
 * («Петров-Водкин»), латиница, цифры и служебные символы — нет.
 */

// Одно слово ФИО: заглавная кириллическая буква + строчные, опциональная
// вторая часть через дефис (двойная фамилия/имя). Ё/ё поддерживаются.
const NAME_PART_RE = /^[А-ЯЁ][а-яё]+(?:-[А-ЯЁ][а-яё]+)?$/;

/** Нормализует ввод: тримминг + схлопывание повторных пробелов. */
export function normalizeFullName(raw: string): string {
	return raw.trim().replace(/\s+/g, " ");
}

/**
 * Возвращает текст ошибки или `null`, если ФИО валидно.
 * Используется и на клиенте (живая подсказка), и на сервере (zod-схема).
 */
export function validateFullName(raw: string): string | null {
	const value = normalizeFullName(raw);
	if (!value) return "Укажите ФИО получателя";

	const parts = value.split(" ");
	if (parts.length < 2) {
		return "Укажите фамилию и имя полностью";
	}
	if (parts.length > 3) {
		return "Слишком много слов — укажите фамилию, имя и отчество";
	}
	if (!parts.every((part) => NAME_PART_RE.test(part))) {
		return "Введите настоящее ФИО кириллицей, например «Иванов Иван Иванович»";
	}
	return null;
}

export function isValidFullName(raw: string): boolean {
	return validateFullName(raw) === null;
}
